package EN;

/**
 *
 * @author Cana
 */
public class Order {
    private int minor;
    private int medium;
    private int higher;

    /**
     * @return the minor
     */
    public int getMinor() {
        return minor;
    }

    /**
     * @param minor the minor to set
     */
    public void setMinor(int minor) {
        this.minor = minor;
    }

    /**
     * @return the medium
     */
    public int getMedium() {
        return medium;
    }

    /**
     * @param medium the medium to set
     */
    public void setMedium(int medium) {
        this.medium = medium;
    }

    /**
     * @return the higher
     */
    public int getHigher() {
        return higher;
    }

    /**
     * @param higher the higher to set
     */
    public void setHigher(int higher) {
        this.higher = higher;
    }
    
}
